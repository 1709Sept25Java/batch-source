public class Q9_NumArray {
	//method for printing out the prime numbers from 1-100
	public static void main (String[] args)
	   {
	      System.out.println ("Here are the Prime Numbers from 1 to 100:  ");
	       for (int index = 1; index < 100; index++)
	      {
	         if (index%2 != 0 && index%2 !=0)
	         System.out.print (index + " ");         
	      }
	   }
	}


